MONEY = {};
MONEY.CONFIG = {};
MONEY.CONFIG.xPos = 0;
MONEY.CONFIG.yPos = 0;
MONEY.CONFIG.title = "Richest Person on the Server";

MONEY.getRichTable = function ()
   local plys = player.GetAll();
   table.sort(plys, function(a,b)
   	  local x = a:getDarkRPVar("money")
   	  local y = b:getDarkRPVar("money")
   	  if x == nil then return end
   	  if y == nil then return end 
      return x > y;
   end)
   return plys;
end

